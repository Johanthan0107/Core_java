class Swaping { 
	public static void main(String[] args) { 
 int a, b; 
 a = 15; 
 b = 10; System.out.println("Before swapping : a,b = "+a+", "+b+" "); 
 a = a + b;
 b = a - b; 
 a = a - b; System.out.println("After swapping : a,b = "+a+", "+b+" ");//we can use + + b(include space in mid of pulse
}
}